function intToBuffer(num) {
    const buf = Buffer.alloc(4);
    // 取低 32 位（无符号），高位自动丢弃
    const truncated = BigInt(num) & 0xFFFFFFFFn;
    buf.writeUInt32LE(Number(truncated), 0);
    return buf;
}

